package byow.Core;

public class Position {
    int X;
    int Y;


    Position(int x, int y) {
        X = x;
        Y = y;
    }

    public int getX() {
        return X;
    }
    public int getY() {
        return Y;
    }
    public void setX(int x) {
        this.X = x;
    }
    public void setY(int y) {
        this.Y = y;
    }

}
